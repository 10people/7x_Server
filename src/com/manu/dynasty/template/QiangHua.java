package com.manu.dynasty.template;

public class QiangHua {
	private int id;
	private int qianghuaId;
	private String name;
	private int level;
	private int gongji;
	private int fangyu;
	private int shengming;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQianghuaId() {
		return qianghuaId;
	}
	public void setQianghuaId(int qianghuaId) {
		this.qianghuaId = qianghuaId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getGongji() {
		return gongji;
	}
	public void setGongji(int gongji) {
		this.gongji = gongji;
	}
	public int getFangyu() {
		return fangyu;
	}
	public void setFangyu(int fangyu) {
		this.fangyu = fangyu;
	}
	public int getShengming() {
		return shengming;
	}
	public void setShengming(int shengming) {
		this.shengming = shengming;
	}
	
}
