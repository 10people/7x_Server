package com.manu.dynasty.template;

public class TiLi {
	public int ID;
	public String start;
	public String end;
	public int tili;
	public int cost;
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public int getTili() {
		return tili;
	}
	public void setTili(int tili) {
		this.tili = tili;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
}
