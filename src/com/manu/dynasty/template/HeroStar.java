package com.manu.dynasty.template;

public class HeroStar {
	private int id;
	private int quality;
	private int star;
	private int nextStar;
	private int exp;
	private int jingmaidian;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQuality() {
		return quality;
	}
	public void setQuality(int quality) {
		this.quality = quality;
	}
	public int getStar() {
		return star;
	}
	public void setStar(int star) {
		this.star = star;
	}
	public int getNextStar() {
		return nextStar;
	}
	public void setNextStar(int nextStar) {
		this.nextStar = nextStar;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getJingmaidian() {
		return jingmaidian;
	}
	public void setJingmaidian(int jingmaidian) {
		this.jingmaidian = jingmaidian;
	}
}
