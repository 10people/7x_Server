package com.manu.dynasty.template;

public class JingPo {
	private int id;
	private int hechengNum;
	private int heroId;
	private int wujiangExp;
	private int fenjieNum;
	private int exp;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHechengNum() {
		return hechengNum;
	}
	public void setHechengNum(int hechengNum) {
		this.hechengNum = hechengNum;
	}
	public int getHeroId() {
		return heroId;
	}
	public void setHeroId(int heroId) {
		this.heroId = heroId;
	}
	public int getWujiangExp() {
		return wujiangExp;
	}
	public void setWujiangExp(int wujiangExp) {
		this.wujiangExp = wujiangExp;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public int getFenjieNum() {
		return fenjieNum;
	}
	public void setFenjieNum(int fenjieNum) {
		this.fenjieNum = fenjieNum;
	}
	
}
